package ass2.spec;

import com.jogamp.opengl.util.texture.Texture;

public class TexturePack {
  private Texture terrain;
  private Texture treeTrunk;
  private Texture treeLeaves;
  private Texture road;
  private Texture avatar;
  private Texture enemyBody;
  private Texture enemyEyes;
  private Texture enemyMouth;
  
  public Texture getTerrain() {
    return terrain;
  }
  
  public void setTerrain(Texture terrain) {
    this.terrain = terrain;
  }
  
  public Texture getTreeTrunk() {
    return treeTrunk;
  }
  
  public void setTreeTrunk(Texture treeTrunk) {
    this.treeTrunk = treeTrunk;
  }
  
  public Texture getTreeLeaves() {
    return treeLeaves;
  }
  
  public void setTreeLeaves(Texture treeLeaves) {
    this.treeLeaves = treeLeaves;
  }
  
  public Texture getRoad() {
    return road;
  }
  
  public void setRoad(Texture road) {
    this.road = road;
  }
  
  public Texture getAvatar() {
    return avatar;
  }
  
  public void setAvatar(Texture avatar) {
    this.avatar = avatar;
  }
  
  public Texture getEnemyBody() {
    return enemyBody;
  }
  
  public void setEnemyBody(Texture enemyBody) {
    this.enemyBody = enemyBody;
  }
  
  public Texture getEnemyEyes() {
    return enemyEyes;
  }
  
  public void setEnemyEyes(Texture enemyEyes) {
    this.enemyEyes = enemyEyes;
  }
  
  public Texture getEnemyMouth() {
    return enemyMouth;
  }
  
  public void setEnemyMouth(Texture enemyMouth) {
    this.enemyMouth = enemyMouth;
  }
}
